""" This function helps you to find sum of squares of three numbers."""


class AddSq3:
    """ This class has a function which helps you to find sum of squares of three numbers."""

    def AddSq3(self, a, b, c):
        """Asso

        Parameters:
        Send three integer parameters(a,b,c).   
        Returns:
        Sum of squares of three numbers(a^2 + b^2 + c^2)."""

        return a ^ 2 + b ^ 2 + c ^ 2
