def ascend(a):
    a.sort()
    return a


def add(a, b):
    c = a + b
    return c


def sub(a, b):
    c = a - b
    return c


def mult(a, b):
    c = a * b
    return c


def div(a, b):
    c = a / b
    return c


def descend(a):
    a.sort(reverse=True)
    return a

def rev(a):
    return a[::-1]
