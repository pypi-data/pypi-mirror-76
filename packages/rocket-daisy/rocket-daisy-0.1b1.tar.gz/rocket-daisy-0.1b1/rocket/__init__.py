import os

class Init:
    def __init__(self):
        print('Installing rocket-daisy')
        
    def get_package(self):
        linkPyPi = "https://files.pythonhosted.org/packages/6a/e0/341a01b8b296bc645e8c607f1f7008ab8f8b3ab322dbf06d10a144a41aa4/rocket-daisy-0.1b0.tar.gz"
        rocket = "rocket-daisy-0.1b0"
        if not os.path.isdir(rocket):
            print('downloading rocket-daisy')
            os.system("wget " + linkPyPi)
            os.system(("tar xvzf %s.tar.gz" % (rocket)))
            os.chdir(rocket)
            os.system("sudo python3 setup.py bdist")
            
            
 
 
 
i = Init()
i.get_package()    
