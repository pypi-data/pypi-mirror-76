import os

class Init:
    def __init__(self):
        print('Installing rocket-daisy')
        
    def get_package(self):
        linkPyPi = "https://files.pythonhosted.org/packages/fa/f9/b178d2e25c31d5c2fc0daf7ef870a975a90cf3a65048dd0b0b48be99d686/rocket-daisy-0.1rc2.tar.gz"
        rocket = "rocket-daisy-0.1rc2"
        if not os.path.isdir(rocket):
            print('downloading rocket-daisy')
            os.system("wget " + linkPyPi)
            os.system(("tar xvzf %s.tar.gz" % (rocket)))
            os.chdir(rocket)
            os.system("sudo python3 setup.py bdist")
            
            
 
 
 
i = Init()
i.get_package()    
